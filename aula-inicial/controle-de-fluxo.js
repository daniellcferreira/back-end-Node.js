const num1 = 100;
const num2 = 20;

if (num1 > 1) {
  console.log("Este numero é maior que 1");
}

if (num1 > 200) {
  console.log("Este numero é maior que 200");
} else {
  console.log("Este numero é menor que 200");
}

if (num2 < num1) {
  console.log(`${num2} é menor que ${num1}`);
}

if (num2 === num1) {
  console.log("Os numero são iguais");
} else {
  console.log("Os numero são difetentes");
}
